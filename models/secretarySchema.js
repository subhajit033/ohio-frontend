const mongoose = require("mongoose");

const secretarySchema = new mongoose.Schema({
  username: {
    type: String,
    unique: true,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Secretary = mongoose.model("Secretary", secretarySchema);
module.exports = Secretary;
